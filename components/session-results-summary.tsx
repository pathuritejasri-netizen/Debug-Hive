'use client';

import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Copy, Download } from 'lucide-react';

interface AIResult {
  agent: string;
  result: string;
  status: 'pending' | 'complete' | 'error';
}

interface SessionResultsSummaryProps {
  title: string;
  code: string;
  language: string;
  aiResults: Record<string, AIResult>;
  onExport?: (format: 'markdown' | 'json') => void;
}

export function SessionResultsSummary({
  title,
  code,
  language,
  aiResults,
  onExport,
}: SessionResultsSummaryProps) {
  const copyToClipboard = (text: string) => {
    navigator.clipboard.writeText(text);
  };

  const exportMarkdown = () => {
    let markdown = `# Debug Session Report: ${title}\n\n`;
    markdown += `**Language:** ${language}\n\n`;
    markdown += `## Original Code\n\`\`\`${language}\n${code}\n\`\`\`\n\n`;

    Object.entries(aiResults).forEach(([agent, result]) => {
      const agentNames: Record<string, string> = {
        'bug-finder': 'Bug Finder',
        'explainer': 'Code Explanation',
        'fixer': 'Suggested Fixes',
        'tester': 'Testing Analysis',
        'reviewer': 'Code Review',
      };

      markdown += `## ${agentNames[agent] || agent}\n${result.result}\n\n`;
    });

    markdown += `---\n*Generated by DebugHive - AI-powered collaborative debugging platform*\n`;

    const blob = new Blob([markdown], { type: 'text/markdown' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `debug-session-${title.replace(/\s+/g, '-')}.md`;
    a.click();
  };

  const exportJSON = () => {
    const data = {
      title,
      language,
      code,
      results: aiResults,
      exportedAt: new Date().toISOString(),
    };

    const blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `debug-session-${title.replace(/\s+/g, '-')}.json`;
    a.click();
  };

  return (
    <div className="space-y-4">
      <Card className="bg-slate-900 border-slate-800">
        <CardHeader>
          <div className="flex justify-between items-start">
            <div>
              <CardTitle className="text-white">{title}</CardTitle>
              <CardDescription className="text-slate-400">{language}</CardDescription>
            </div>
            <div className="flex gap-2">
              <Button
                size="sm"
                variant="outline"
                onClick={exportMarkdown}
                className="text-slate-300 border-slate-700 hover:bg-slate-800"
              >
                <Download className="w-4 h-4 mr-2" />
                Markdown
              </Button>
              <Button
                size="sm"
                variant="outline"
                onClick={exportJSON}
                className="text-slate-300 border-slate-700 hover:bg-slate-800"
              >
                <Download className="w-4 h-4 mr-2" />
                JSON
              </Button>
            </div>
          </div>
        </CardHeader>
        <CardContent>
          <div className="space-y-4">
            <div>
              <h4 className="text-sm font-semibold text-white mb-2">Original Code</h4>
              <div className="relative">
                <pre className="bg-slate-800 p-3 rounded text-xs text-slate-300 overflow-x-auto max-h-40 line-clamp-6">
                  {code}
                </pre>
                <Button
                  size="sm"
                  variant="ghost"
                  onClick={() => copyToClipboard(code)}
                  className="absolute top-2 right-2 text-slate-400 hover:text-white"
                >
                  <Copy className="w-4 h-4" />
                </Button>
              </div>
            </div>

            <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
              {Object.entries(aiResults).map(([agent, result]) => {
                const agentNames: Record<string, string> = {
                  'bug-finder': 'Bug Finder',
                  'explainer': 'Code Explanation',
                  'fixer': 'Suggested Fixes',
                  'tester': 'Testing Analysis',
                  'reviewer': 'Code Review',
                };

                const agentIcons: Record<string, string> = {
                  'bug-finder': '🐛',
                  'explainer': '📚',
                  'fixer': '🔧',
                  'tester': '✅',
                  'reviewer': '👀',
                };

                return (
                  <Card key={agent} className="bg-slate-800 border-slate-700">
                    <CardHeader className="pb-3">
                      <div className="flex items-center gap-2">
                        <span className="text-lg">{agentIcons[agent]}</span>
                        <CardTitle className="text-sm text-white">
                          {agentNames[agent] || agent}
                        </CardTitle>
                      </div>
                    </CardHeader>
                    <CardContent className="pt-0">
                      <div className="relative">
                        <pre className="text-xs text-slate-300 max-h-32 overflow-y-auto whitespace-pre-wrap">
                          {result.result}
                        </pre>
                        <Button
                          size="sm"
                          variant="ghost"
                          onClick={() => copyToClipboard(result.result)}
                          className="absolute top-0 right-0 text-slate-400 hover:text-white"
                        >
                          <Copy className="w-3 h-3" />
                        </Button>
                      </div>
                    </CardContent>
                  </Card>
                );
              })}
            </div>
          </div>
        </CardContent>
      </Card>
    </div>
  );
}
